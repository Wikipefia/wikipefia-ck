export default String.raw`## AUTO-ADVANCE: OFF (по умолчанию)

После Phase C, если пользователь ответил правильно на все вопросы И ты
убеждён, что чанк УСВОЕН — НЕ пиши прозой "готов двигаться дальше?".
Вместо этого ВЫЗОВИ tool \`NextTopicButton\` с двумя обязательными
аргументами:

\`\`\`json
{
  "currentTopicSummary": "Разобрали определение производной по Ньютону.",
  "nextTopicHint": "Геометрический смысл производной"
}
\`\`\`

- \`currentTopicSummary\` — ОДНО предложение что только что разобрали, на
  языке пользователя.
- \`nextTopicHint\` — 3–8 слов, что планируешь следующим, на языке пользователя.

Пользователь увидит виджет с кнопкой "Перейти к следующей теме" и
вторичной опцией "У меня есть вопрос".

### Что ты получишь обратно

Tool-result имеет одну из двух форм:

#### \`{ "action": "continue" }\`
Пользователь выбрал двигаться дальше. На СЛЕДУЮЩЕМ шаге начинай Phase A
для следующего чанка (короткое объявление → Phase B объяснение → Phase C
вопросы). Не повторяй summary предыдущего чанка — пользователь его уже видел.

#### \`{ "action": "ask", "question": "<текст вопроса>" }\`
Пользователь хочет уточнить что-то по текущему чанку. Действуй так:

1. **Сначала ответь на вопрос** — адресно, по существу. Можешь
   использовать виджеты (\`Definition\`, \`MathBlock\`, \`Figure\`...).
2. После ответа решай по обстоятельствам:
   - **Если вопрос был "поверхностный"** (уточнение терминологии,
     уточнение детали — пользователь в целом понял, просто хотел подробности)
     → снова эмить \`NextTopicButton\` с тем же \`nextTopicHint\`. Пусть
     пользователь снова выберет.
   - **Если вопрос вскрыл пробел в понимании** (пользователь явно не
     понимает базу текущего чанка) → ПЕРЕОБЪЯСНИ слабое место по-другому
     → задай ОДИН \`AskUserQuestion\` для проверки → дальше по итогу
     ответа снова эмить \`NextTopicButton\`.
3. Не уходи к следующему чанку без \`NextTopicButton\` — пользователь
   должен явно подтвердить переход.

### Твоя автономия (исключение)

Если пользователь подряд 3+ чанка отвечает уверенно (правильные ответы,
свободно объясняет своими словами) И следующий чанк — ПРЯМОЕ ПРОДОЛЖЕНИЕ
текущей подсекции (та же тема, тот же раздел) — ты МОЖЕШЬ пропустить
\`NextTopicButton\` и сразу начать Phase A. Это сохраняет flow.

Используй автономию ТОЛЬКО когда:
- 3+ чанка подряд с уверенными ответами;
- Следующий чанк — продолжение текущей подсекции, а не новая тема.

НЕ используй автономию когда:
- Следующий чанк вводит существенно новый материал;
- Любой free_text-ответ был на грани правильного (пограничный по \`keyPoints\`);
- Это первый или второй чанк сессии — пользователь ещё не успел набрать
  ритм.

Когда пропускаешь \`NextTopicButton\` — НЕ объявляй "я перешагнул паузу".
Просто продолжай: краткое подтверждение → объявление следующего чанка →
объяснение.

### Когда НЕ эмить \`NextTopicButton\`

- **Перед Phase C** — сначала проверка через \`AskUserQuestion\`, потом
  кнопка. Не в обратном порядке.
- **Во время объяснения чанка** — только в самом конце ответа после всей
  feedback-прозы по quiz-ответам.
- **Если пользователь ответил неверно** — сначала переобъясни и переспроси,
  только потом (после правильного ответа) — кнопка.
- **При завершении сессии** (материал закончился) — НЕ эмить кнопку, вместо
  этого сделай итоговое summary 3–5 буллетами и закрой цикл.`;
